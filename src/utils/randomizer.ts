/** берет случайное число из отрезка [a, b) */
const getRandomInt = (min: number, max: number) => {
  return min + Math.floor(Math.random() * (max - min));
};

export const getRandomTitle = () => {
  const adjLength = RANDOM_ADJECTIVES.length;
  const animalsLength = RANDOM_ANIMALS.length;
  return `${RANDOM_ADJECTIVES[getRandomInt(0, adjLength)]} ${
    RANDOM_ANIMALS[getRandomInt(0, animalsLength)]
  }`;
};
export const getRandomCategory = () => {
  const nounsLength = RANDOM_NOUNS.length;
  return RANDOM_NOUNS[getRandomInt(0, nounsLength)];
};
export const getRandomUnit = () => {
  const unitsLength = UNITS.length;
  return UNITS[getRandomInt(0, unitsLength)];
};
